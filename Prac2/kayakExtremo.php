<h2>Islandia: país de cascadas</h2>
<p>Una cascada es -probablemente- la formación más estética e hipnótica que podemos encontrar en un río. Y si hay un país que 
destaca por sus cascadas, ese es Islandia; un país que está viviendo un boom turístico, cimentado principalmente en su impactante 
Naturaleza: volcanes, glaciares, aguas termales, géiseres y, por su puesto, cascadas. Skogafoss, Godafoss, Dettifoss, Svartifoss... son sólo algunas de 
las paradas obligatorias que llenan revistas y redes sociales de impactantes fotos. No es de extrañar, por tanto, el interés que suscita esta pequeña 
isla perdida en el Ártico entre los kayakistas extremos.
A principios de año, el equipo charló con Aniol Serrasolses, uno de los mayores referentes del mundo del kayak extremo y compañero de 
remadas en muchas aventuras, sobre la opción de ir a Islandia a intentar abrir ríos nuevos. Tras un año parados, tenían ganas de arrancar 
de nuevo y esta isla era una buena opción. Él ya había estado por allí y había explorado el terreno antes, abriendo, además, unos cuantos ríos 
muy interesantes. Les confirmó que aún quedaba mucho mundo por descubrir en esas tierras y que le encantaría adentrarse en la zona en busca de nuevas aventuras.</p>
<p>Y así surgió la idea de filmar este documental. Como suele ser habitual en proyectos audiovisuales sobre el deporte del kayak, 
ellos remarían y también se encargarían de la filmación, y en este caso, incluso de la dirección y edición de la pieza final.
Les era clave, por tanto, contar con un cámara que fuera kayakista de primer nivel, así que se sumó al 
grupo Aleix Salvat, experto kayakista y profesional del mundo audiovisual.
Al final, el proyecto se fue haciendo cada vez más grande y, a última hora, se les sumó al grupo David Nogales, 
un fotógrafo venido del mundo del MTB, que les echaría una mano con toda la logística, con la filmación, y por supuesto, a la hora de capturar buenas instantáneas.</p>
